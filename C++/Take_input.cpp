#include <iostream>
using namespace std;
int main()
{
    int x, y;
    cout << "Enter the value of x and y: ";
    cin >> x >> y;
    cout << "The value of x is: " << x << endl;
    cout << "The square of the entered number is :" << x * y << endl;
    cout << "The sum of the entered number is :" << x + y << endl;
    cout << "The subtract of the entered number is :" << x - y << endl;
    cout << "The divide of the entered number is :" << x / y << endl;
    cout << "The remainder when x is divided by y is :" <<x%y<< endl;
}